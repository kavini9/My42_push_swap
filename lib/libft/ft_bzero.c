#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n--)
	{
		*(char *)s = 0;
		s++;
	}
}
/******************************************************************************/
/*                                                                            */
/*DESCRIPTION                                                                 */
/*	   Bzero writes n zeroed bytes to the string s.  If n is zero, bzero does */
/*	   nothing.                                                               */
/*                                                                            */
/******************************************************************************/
